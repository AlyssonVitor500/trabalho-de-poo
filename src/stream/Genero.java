package stream;

public class Genero {
    private Long codigoDoGenero;
    private String nomeGenero;

    public Long getCodigoDoGenero() {
        return codigoDoGenero;
    }

    public void setCodigoDoGenero(Long codigoDoGenero) {
        this.codigoDoGenero = codigoDoGenero;
    }

    public String getNomeGenero() {
        return nomeGenero;
    }

    public void setNomeGenero(String nomeGenero) {
        this.nomeGenero = nomeGenero;
    }
}
